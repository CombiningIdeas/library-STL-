#include <iostream>
#include <forward_list>

using namespace std;

int main()
{
	setlocale(LC_ALL, "Russian");

	forward_list<int> f1 = { 4321,7983, 9032 };

	// Этот итератор указывает на самый первый элемент, тоесть под индексом 0
	forward_list<int>::iterator it = f1.begin();

	// Этот итератор указывает на адрес, который идет перед самым первым элементом
	// это нужно для того, чтобы можно было вставить потом элемент на нулевой индекс
	forward_list<int>::iterator it_2 = f1.before_begin();


	// как раз таки эта команда вставляет элемент, но только после 
	// того элемента который мы укажем, поэтому и нужен метод before_begin()
	// тут вставка элемента отличается, потому что мы вставляем элемент после, 
	// а не перед как обычно, но все дело в том, что это односвязный список и 
	// элементы тут хранят указатель на следующий элемент, и не хранят на предыдущий
	// поэтому вставка происходит после того элемента, который мы указываем в методе
	// insert_after(a, b);
	f1.insert_after(it_2, 777);

	// Можно двумя способами вывести односвязный список с помощью цикла, так же можно
	// было сделать и с двухсвязным списком и с любым массивом тоже.
	cout << "Односвязнный список: " << endl;
	for (auto element : f1)
	{
		cout << element << ' ';
	}
	cout << endl;


	cout << "Односвязнный список: " << endl;
	for (forward_list<int>::iterator ii = f1.begin(); ii != f1.end(); ii++)
		cout << *ii << ' ';
	cout << endl;


	return 0;
}